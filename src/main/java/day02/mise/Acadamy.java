package day02.mise;

public class Acadamy {
		String Name;
		int Id;
		String Code;
		
		public void setName(String newName) {
			Name = newName;
		}
		public String getName() {
			return Name;
		}
		public void setId(int newId) {
			Id = newId;
		}
		public int getId() {
			return Id;
		}
		public void setCode(String newCode) {
			Code = newCode;
		}
		public String getCode() {
			return Code;
		}
		
}

